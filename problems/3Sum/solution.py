def two_sum(nums: list, target_sum: int, ignore_index: int) -> list:
    results = []

    other_halves = {target_sum - num: (num, idx) for idx, num in enumerate(nums)}

    for idx, num in enumerate(nums):
        if idx == ignore_index:
            continue

        if num in other_halves and other_halves[num][1] not in (idx, ignore_index):
            results.extend([num, other_halves[num][0]])

    return results


def solution(nums: list):
    """
    :type nums: List[int]
    :rtype: List[List[int]]
    """
    output = []

    if len(nums) < 3:
        return output

    stored_results = {}

    for idx, num in enumerate(nums):

        results = two_sum(nums[:-1], 0 - num, idx)

        for two_sum_result in results:

            if two_sum_result \
                    and (num not in stored_results or sorted([num] + two_sum_result) not in stored_results[num]):
                traverse_list = sorted([num] + two_sum_result)

                for n in traverse_list:
                    if n not in stored_results:
                        stored_results[n] = []

                    if traverse_list not in stored_results[n]:
                        stored_results[n].append(traverse_list)

                output.append(traverse_list)

    return output


